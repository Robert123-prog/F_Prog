import turtle

t = turtle.Pen()


def fwd():
    t.speed(1)
    t.forward(10)

def bckw():
    t.speed(1)
    t.forward(-10)

def rotright():
    t.speed(1)
    t.right(45)

def rotleft():
    t.speed(1)
    t.left(45)

def pup():
    t.speed(1)
    t.up()

def pdown():
    t.speed(1)
    t.down()




